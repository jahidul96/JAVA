public class SwitchCondition {
    public static void main(String[] args) {

        String number = "five";
        switch(number){
            case "five" :
                System.out.println("number is 5");
                break;
            case "two" :
                System.out.println("number is 2");
                break;
            default :
            System.out.println("don't know!!");
            break;

        }
    }
}


class Variable {
    public static void main(String[] args){
        // variable defination
        String name = "20";
        int age = 23;
        float number = 20.3F;
        boolean isAdmin = true;
        System.out.println(name + "is" + age + "year's old!");
        System.out.println(isAdmin);
        System.out.println(number + age);

        // instance check
        System.out.println(name instanceof String);
    }
}
